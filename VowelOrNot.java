package LabBooks;

public class VowelOrNot {
public static void main(String[] args) {
	char ch = 'b';
	if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u') {
		System.out.println("Entered character is VOWEL");
	}
	else {
		System.out.println("Entered character is CONSONANT");
	}
}
}
